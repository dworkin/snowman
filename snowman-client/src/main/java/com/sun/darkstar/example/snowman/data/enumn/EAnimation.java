package com.sun.darkstar.example.snowman.data.enumn;

/**
 * <code>EAnimation</code> defines the enumerations of all <code>JointAnimation</code>
 * used by the <code>Game</code>.
 * 
 * @author Yi Wang (Neakor)
 * @version Creation date: 06-10-2008 17:14 EST
 * @version Modified date: 08-04-2008 12:32 EST
 */
public enum EAnimation {
	/**
	 * The snowman idle animation.
	 */
	Idle,
	/**
	 * The snowman movement animation.
	 */
	Move,
	/**
	 * The snowman attack animation.
	 */
	Attack,
	/**
	 * The snowman hit animation.
	 */
	Hit,
	/**
	 * The snowman grabbing animation.
	 */
	Grabbing,
	/**
	 * The snowman death animation.
	 */
	Death,
	/**
	 * The snowman respawn animation.
	 */
	Respawn
}
