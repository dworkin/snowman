package com.sun.darkstar.example.snowman.game.input.enumn;

/**
 * <code>EInputType</code> defines the enumerations of types of inputs that
 * a converter or controller should be registered to.
 * 
 * @author Yi Wang (Neakor)
 * @version Creation date: 07-17-2008 23:15 EST
 * @version Modified date: 07-23-2008 11:26 EST
 */
public enum EInputType {
	/**
	 * The keyboard input type.
	 */
	Keyboard,
	/**
	 * The mouse input type.
	 */
	Mouse,
	/**
	 * The enumeration that flags this controller should not be registered.
	 */
	None
}
